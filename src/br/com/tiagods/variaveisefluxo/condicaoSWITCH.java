package br.com.tiagods.variaveisefluxo;

public class condicaoSWITCH {

	public static void main(String[] args){
		
		int i=10;
		
		switch(i){
			
			case 1:
				System.out.println("Numero 2");
			break;
			
			case 2:
				System.out.println("Numero 1");
			break;
			
			default:
				System.out.print("Numero encontrado é 10");
			break;
		
		}
	}
}
